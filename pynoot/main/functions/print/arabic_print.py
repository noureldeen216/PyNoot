import builtins
import sys
from typing import Any, TextIO

def اطبع(*القيم: Any, فاصل: str = ' ', نهاية: str = '\n', ملف: TextIO = sys.stdout, تدفق: bool = False) -> None:
    """
    📣 **اطبع (Print)**

    **شغلانتها**: بتطلع البيانات (الـ`القيم`) اللي بتديها ليها على الشاشة. 
    دي هي الطريقة اللي بتستخدمها عشان تشوف نتايج الكود بتاعك.

    ---
    
     📝 المكونات اللي بتستخدمها:

     القيم (\*القيم)**: أي حاجة عايز تظهرها (ممكن تكون نصوص، أرقام، أو أي متغيرات). تقدر تحط كذا قيمة.
    
        فاصل (sep)**: الحرف أو المسافة اللي هتتحط بين كل قيمة والتانية.
        القيمة الافتراضية**: مسافة (`' '`).
        مثال**: لو عايز تفصل بين الكلام بشرطة، حط `فاصل='-'`.
    
     نهاية (end)**: الحرف اللي الدالة بتختم بيه السطر بعد ما بتطبع كل حاجة.
        القيمة الافتراضية**: سطر جديد (`\n`).
        مثال**: لو عايز الكلمة اللي بعدها تظهر في نفس السطر، حط `نهاية=' '`.
    
      ملف (file)**: بتحدد المكان اللي هتطبع فيه (عادةً بيكون الشاشة).
    
      تدفق (flush)**: بتخلي البيانات تظهر على طول حتى لو لسه مش مالية الـBuffer. (عادةً مش محتاجها).
    
    ---

     ✨ مثال سريع:
    
    اطبع("أهلا", "ببرنامج", "باي نطق!", فاصل=" - ") 
    # النتيجة اللي هتظهر: أهلا - ببرنامج - باي نطق!
    """
    # الكود اللي بينفذ الأمر الحقيقي للطباعة
    builtins.print(*القيم, sep=فاصل, end=نهاية, file=ملف, flush=تدفق)