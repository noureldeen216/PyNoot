import builtins
from typing import Iterable, Any, Callable, List

def رتب(المجموعة: Iterable[Any], المفتاح: Callable[[Any], Any] = None, عكسي: bool = False) -> List[Any]:
    """
    🗄️ **رتب (Sort)**

    **شغلانتها**: بترجع **قائمة جديدة** مرتبة من أي مجموعة بيانات (قائمة، مجموعة، قاموس، إلخ) بتديها ليها.
    الدالة الأصلية بتاعت بايثون اللي بتستخدمها اسمها `sorted()`.

    ---
    
    ### 📝 المكونات اللي بتستخدمها:

    * **المجموعة (iterable)**: هي مجموعة البيانات اللي عايز ترتبها (زي قائمة الأرقام أو الأسماء).
    
    * **المفتاح (key)**: دالة (Function) اختيارية بتحدد **على أساس إيه** الترتيب هيتم.
        * **مثال**: لو عندك قائمة من أسماء ناس، وعايز ترتبهم حسب طول الاسم، بتحط هنا دالة بتحسب طول الاسم.
        * **القيمة الافتراضية**: None (الترتيب بيكون على أساس القيمة نفسها).
    
    * **عكسي (reverse)**: قيمة منطقية (True أو False) بتحدد اتجاه الترتيب.
        * `عكسي=False` (الافتراضي): ترتيب **تصاعدي** (من الصغير للكبير، أو من أ لـ ي).
        * `عكسي=True`: ترتيب **تنازلي** (من الكبير للصغير، أو من ي لـ أ).
    
    ---

    ### ↩️ القيمة المرتجعة (Return Value):
    
    * الدالة بترجع دائمًا **قائمة جديدة** مترتبة (List) من نفس أنواع البيانات اللي كانت في المجموعة الأصلية.

    ---

    ### ✨ مثال سريع:
    
    الأرقام = [50, 10, 80, 20]
    
    # مثال 1: ترتيب تصاعدي عادي (الافتراضي)
    المرتبة = رتب(الأرقام)
    # النتيجة: [10, 20, 50, 80]

    # مثال 2: ترتيب تنازلي
    المرتبة_عكسي = رتب(الأرقام, عكسي=True)
    # النتيجة: [80, 50, 20, 10]
    """
    # الكود اللي بينفذ الأمر الحقيقي للترتيب
    # builtins.sorted هي الدالة الأصلية في بايثون

    return builtins.sorted(المجموعة, key=المفتاح, reverse=عكسي)






