
# clear() In arabic

import os

def مسح_الكل():
    """
    🧹 **مسح_الكل (Clear Screen)**

    **شغلانتها**: بتمسح كل الكلام اللي مكتوب في شاشة الأوامر (الـ Console) عشان تخلي الشاشة نضيفة وزي الفل.
    
    ---

    ### 📝 المكونات:
    * **الدالة دي مابتاخدش أي حاجة بين القوسين**.
    
    ### ↩️ القيمة المرتجعة:
    * مابترجعش حاجة (None)، هي بس بتنضف الشاشة.

    ---

    ### ✨ مثال:
    # بعد ما تطبع كلام كتير وعايز تمسحه:
    مسح_الكل() 
    """
    # الكود الفعلي بيجي بعد الـ Docstring علطول
    if os.name == 'nt':  # لو شغال على ويندوز
        os.system('cls')
    else:                # لو شغال على ماك أو لينكس
        os.system('clear')