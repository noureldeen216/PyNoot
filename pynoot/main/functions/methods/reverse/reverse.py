def اعكس(المجموعة):
    """
    🔄 **اعكس (Reverse)**

    **شغلانتها**: بتشقلب حال القائمة! بتخلي اللي كان واقف في آخر الطابور يبقى هو اللي في الأول، والعكس صحيح.
    
    ---

    ### 📝 المكونات:
    * **المجموعة**: القائمة اللي أنت عايز تقلب كيانها وتغير ترتيبها.
    
    ### ↩️ القيمة المرتجعة:
    * بتعدل في القائمة الأصلية **فوراً** (In-place)، يعني مش محتاج تخزنها في متغير جديد.

    ---

    ### ✨ مثال:
    الترتيب = ["الأول", "الثاني", "الثالث"]
    اعكس(الترتيب)
    # النتيجة هتبقى: ["الثالث", "الثاني", "الأول"]
    """
    المجموعة.reverse()