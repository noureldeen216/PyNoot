import builtins
from typing import List, Any

def ادخل(الرسالة: str = None) -> str:
    """
    📥 **ادخل (Input)**

    **شغلانتها**: بتخلي المستخدم يدخل بيانات (نص) من لوحة المفاتيح.
    دي هي الطريقة اللي بتستقبل بيها معلومات من اللي بيستخدم البرنامج بتاعك.
    
    ---

    ### 📝 المكونات اللي بتستخدمها:

    * **الرسالة (prompt)**: النص اللي بيظهر للمستخدم قبل ما يطلب منه إدخال البيانات.
        * **القيمة الافتراضية**: لا يوجد (None).
        * **مثال**: `الرسالة="كم عمرك؟ "`

    ### ↩️ القيمة المرتجعة (Return Value):
    
    * الدالة بترجع (ترجع) القيمة اللي دخلها المستخدم **دايما كنص (String)**، حتى لو دخل أرقام.

    ---

    ### ✨ مثال سريع:
    
    الاسم = ادخل("إيه اسمك يا معلم؟ ")
    اطبع("أهلا يا", الاسم)
    
    **النتيجة**:
    إيه اسمك يا معلم؟ [المستخدم يكتب اسمه هنا]
    أهلا يا [الاسم اللي كتبه المستخدم]
    """
    # الكود اللي بينفذ الأمر الحقيقي لاستقبال المدخلات
    # builtins.input هي الدالة الأصلية في بايثون
    return builtins.input(الرسالة)